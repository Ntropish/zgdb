import fs from "fs";
import path from "path";
import { execSync } from "child_process";
import { z } from "zod";

// --- Type Definitions for Configuration ---
type ZodSchema = z.ZodObject<any>;
interface EdgeConfig {
  source: string;
  target: string;
  cardinality: "one-to-one" | "one-to-many";
  name: {
    forward: string;
    backward: string;
  };
}
interface GraphConfig {
  schema: {
    nodes: Record<string, ZodSchema>;
    edges: EdgeConfig[];
  };
}

// --- Main Generator Function ---
export function generate(configPath: string, outputPath: string): void {
  const config = loadConfig(configPath);
  validateConfig(config);
  fs.mkdirSync(outputPath, { recursive: true });

  const fbsSchema = generateFlatbuffersSchema(config);
  const fbsPath = path.join(outputPath, "schema.fbs");
  fs.writeFileSync(fbsPath, fbsSchema);
  console.log("Generated FlatBuffers schema:", fbsPath);

  runFlatc(outputPath, fbsPath);
  console.log("Successfully ran flatc compiler.");

  const clientFiles = generateTypeScriptClient(config, outputPath);
  for (const [fileName, content] of Object.entries(clientFiles)) {
    const filePath = path.join(outputPath, fileName);
    fs.writeFileSync(filePath, content);
    console.log(`Generated client file: ${filePath}`);
  }
}

// --- Helper Functions (loadConfig, validateConfig, runFlatc are unchanged) ---
function loadConfig(configPath: string): GraphConfig {
  const configModule = require(configPath);
  return configModule.default;
}

function validateConfig(config: any): asserts config is GraphConfig {
  if (!config?.schema?.nodes || !config?.schema?.edges) {
    throw new Error(
      "Invalid configuration: must contain schema.nodes and schema.edges"
    );
  }
}

function runFlatc(outputPath: string, fbsPath: string): void {
  try {
    const flatcCommand = `flatc --ts -o "${outputPath}" "${fbsPath}"`;
    execSync(flatcCommand, { stdio: "inherit" });
  } catch (error) {
    console.error("Error running flatc. Is it installed and in your PATH?");
    throw error;
  }
}

function generateFlatbuffersSchema(config: GraphConfig): string {
  let fbs = `// Generated by zg. Do not edit.\n\n`;
  fbs += `namespace Graph;\n\n`;

  const zodToFbs: Record<string, string> = {
    ZodString: "string",
    ZodNumber: "double",
    ZodBoolean: "bool",
    ZodDate: "string",
    ZodUuid: "string",
  };

  // Generate a table for each node, INCLUDING relationship IDs
  for (const [name, nodeSchema] of Object.entries(config.schema.nodes)) {
    const capName = capitalize(name);
    fbs += `table ${capName} {\n`;
    // Primitives
    for (const [key, type] of Object.entries(nodeSchema.shape)) {
      const zodType = (type as z.ZodTypeAny)._def.typeName;
      if (zodToFbs[zodType]) {
        fbs += `  ${key}:${zodToFbs[zodType]};\n`;
      }
    }
    // Relationships (as foreign keys)
    for (const edge of config.schema.edges) {
      if (edge.source === name && edge.cardinality === "one-to-one") {
        fbs += `  ${edge.name.forward}_id:string;\n`; // e.g., author_id
      }
      if (edge.target === name) {
        fbs += `  ${edge.name.backward}_id:string;\n`;
      }
    }
    fbs += `}\n\n`;
  }

  // Define the root type for each table
  for (const name of Object.keys(config.schema.nodes)) {
    fbs += `root_type ${capitalize(name)};\n`;
  }

  return fbs;
}

function generateTypeScriptClient(
  config: GraphConfig,
  outputPath: string
): Record<string, string> {
  const nodeNames = Object.keys(config.schema.nodes);

  // --- types.ts ---
  const typesFile = `// Generated by zg. Do not edit.\n
export interface Node {
  readonly id: string;
}

// Generated Node Interfaces
${nodeNames
  .map(
    (name) => `
export interface ${capitalize(name)} extends Node {
  ${generateInterfaceProperties(config, name)}
}
`
  )
  .join("\n")}
`;

  // --- index.ts (The generated client entry point) ---
  const indexFile = `// Generated by zg. Do not edit.
import { flatbuffers } from 'flatbuffers';
import { createClient as createZgClient } from 'zg/runtime';
import type { ClientConfig } from 'zg/runtime';
import { ${nodeNames.map(capitalize).join(", ")} } from './types';
${nodeNames
  .map(
    (name) =>
      `import { ${capitalize(name)} as ${capitalize(
        name
      )}Fbs } from './schema_generated.js';`
  )
  .join("\n")}

const textEncoder = new TextEncoder();
const textDecoder = new TextDecoder();

// --- Schema-aware serialization and configuration ---
const clientConfig: ClientConfig = {
    nodes: {
        ${nodeNames
          .map((name) => `${name}: ${generateNodeConfig(config, name)}`)
          .join(",\n        ")}
    }
};

// --- Public API ---
export function createClient() {
    const zg = createZgClient(clientConfig);
    return zg as {
        ${nodeNames
          .map(
            (name) =>
              `${name}: { add: (data: any) => ${capitalize(
                name
              )}, get: (id: string) => ${capitalize(
                name
              )} | null } & Iterable<${capitalize(name)}>`
          )
          .join(",\n        ")}
    };
}

export * from './types';
`;

  return {
    "types.ts": typesFile,
    "index.ts": indexFile,
  };
}

function generateInterfaceProperties(
  config: GraphConfig,
  nodeName: string
): string {
  const nodeSchema = config.schema.nodes[nodeName];
  let properties = "";
  // Primitives
  for (const [key, type] of Object.entries(nodeSchema.shape)) {
    properties += `${key}: any;\n  `; // Simplified for now
  }
  // Relationships
  for (const edge of config.schema.edges) {
    if (edge.source === nodeName) {
      const targetType = capitalize(edge.target);
      const propType =
        edge.cardinality === "one-to-many" ? `${targetType}[]` : targetType;
      properties += `readonly ${edge.name.forward}: ${propType};\n  `;
    }
    if (edge.target === nodeName) {
      const sourceType = capitalize(edge.source);
      properties += `readonly ${edge.name.backward}: ${sourceType};\n  `;
    }
  }
  return properties;
}

function generateNodeConfig(config: GraphConfig, nodeName: string): string {
  const capName = capitalize(nodeName);
  const nodeSchema = config.schema.nodes[nodeName];
  const primitiveFields = Object.keys(nodeSchema.shape);

  // Create the list of setters for serialization
  const serializers = primitiveFields.map((field) => {
    return `const ${field}Offset = builder.createString(data.${field}?.toString() ?? '');`;
  });

  const creatorCall = primitiveFields
    .map(
      (field) =>
        `${capName}Fbs.add${capitalize(field)}(builder, ${field}Offset);`
    )
    .join("\n    ");

  return `{
    // --- Serialization ---
    serialize: (builder: flatbuffers.Builder, data: any): number => {
        ${serializers.join("\n        ")}
        ${capName}Fbs.start${capName}(builder);
        ${creatorCall}
        return ${capName}Fbs.end${capName}(builder);
    },
    // --- Deserialization ---
    deserialize: (buffer: Uint8Array): any => {
        const buf = new flatbuffers.ByteBuffer(buffer);
        return ${capName}Fbs.getRootAs${capName}(buf);
    },
    // --- Relationships ---
    relations: {
        ${config.schema.edges
          .filter(
            (edge) => edge.source === nodeName || edge.target === nodeName
          )
          .map((edge) => generateRelationConfig(edge, nodeName))
          .join(",\n        ")}
    }
}`;
}

function generateRelationConfig(edge: EdgeConfig, nodeName: string): string {
  if (edge.source === nodeName) {
    // This is a forward relation (e.g., user.posts)
    return `'${edge.name.forward}': {
            kind: '${edge.cardinality}',
            target: '${edge.target}',
            foreignKey: '${edge.name.backward}_id'
        }`;
  } else {
    // edge.target === nodeName
    // This is a backward relation (e.g., post.author)
    return `'${edge.name.backward}': {
            kind: 'one-to-one',
            target: '${edge.source}',
            localKey: '${edge.name.backward}_id'
        }`;
  }
}

function capitalize(s: string): string {
  return s.charAt(0).toUpperCase() + s.slice(1);
}
