import { PTree, TreeConfigOptions } from "prolly-gunna";
import { createNodeSetProxy } from "./proxy";
import flatbuffers from "flatbuffers";

// --- Type Definitions for the Runtime ---

export interface NodeConfig {
  serialize: (builder: flatbuffers.Builder, data: any) => number;
  deserialize: (buffer: Uint8Array) => any;
  relations: Record<string, RelationConfig>;
}

export interface RelationConfig {
  kind: "one-to-one" | "one-to-many";
  target: string;
  localKey?: string; // For resolving one-to-one relations
  foreignKey?: string; // For resolving one-to-many relations
}

export interface ClientConfig {
  nodes: Record<string, NodeConfig>;
  treeConfig?: TreeConfigOptions;
}

export interface GraphContext {
  db: PTree;
  nodeCache: Map<string, any>; // Cache for proxy objects
  config: ClientConfig;
}

/**
 * Creates and initializes a new synchronous graph database client.
 * This is the internal, generic factory used by the generated code.
 * @param clientConfig - The configuration object generated by the build process.
 * @returns A database client object with proxy sets for each node type.
 */
export function createClient(clientConfig: ClientConfig) {
  const db = new PTree(clientConfig.treeConfig);

  const graphContext: GraphContext = {
    db,
    nodeCache: new Map<string, any>(),
    config: clientConfig, // Pass the entire config to the context
  };

  const client: { [key: string]: any } = {};

  for (const nodeName of Object.keys(clientConfig.nodes)) {
    client[nodeName] = createNodeSetProxy(graphContext, nodeName);
  }

  return client;
}
